#Exibe para o usuário para digitar
nome = input("Informe o seu nome: ")
idade = input("Informe a sua idade: ")

#Mostra a saída Mateus 21
print(nome, idade)
#Realiza a quebra de linha \n Mateus 21...
print(nome, idade, end="...\n")
# Utilizando o separador e o end Mateus#21...
print(nome, idade, sep="#", end="...\n")
#Coloca o caracter informado na separação das duas informações. Mateus#21
print(nome, idade, sep="#")




